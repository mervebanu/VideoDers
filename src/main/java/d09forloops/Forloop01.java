package d09forloops;

public class Forloop01 {
    public static void main(String[] args) {
        // ekrana 10 kere "hi" yazdirin

        // Loop (Dongu) : Tekrar tekrar ayni seyi yapmak zorunda kaldigimizda
        // ayni kodu bircok kez yazmak yerine loop lari kullaniriz.

        // for-loop

        for(int a = 1; a<11 ; a++) {
            System.out.println("hi");

        }
        // 100 den  1 ekadar tam sayilari ekrana ayni satirda bosluk birakarak yazdirin
        for(int i = 100; i>=1; i--){
            System.out.print(i + " ");
        }
        }
    }

