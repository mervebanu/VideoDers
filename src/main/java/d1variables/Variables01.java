package d1variables;

public class Variables01 {
    public static void main(String[] args) {

        // Java da cumle(statement) bitince " ; " konulur
        // kod yazarken kirmizi alt cizgi gorurseniz bu bit hata yaptiginiz anlamina gelir bu hatayi duzeltmeden baska kod yazmaya calismayin
        // Bu kirmizi alt cizgiye javada "Compile Time Error" denir
        // compile time error 0-1 lere cevirmede hata anllamina gelir
        System.out.println("Hello World");

        //ekrana alt alta Ali01, Ali02, Ali03 yazdirin
        System.out.println("Ali01");
        System.out.println("Ali02");
        System.out.println("Ali03");

        // Ekrana ayni satira Veli02, Veli02, Veli03 yazdirin
        // Javada space(bosluk) bir karakter olarak kabul edilir.
        System.out.print("Veli01 ");
        System.out.print("Veli02 ");
        System.out.println("Veli03");

        /*
        Ekrana A
              A  A
             A  A  A  YAZDIRINIZ
         */
        System.out.println("  A  ");
        System.out.println(" A A ");
        System.out.println("A A A");
    }



}
